// Generic flex bit unpack (supports 1-10 bits per element)
static __device__ __forceinline__ int tq_flex_unpack(const uint8_t * buf, int idx, int bits) {
    int bp = idx * bits, bi = bp / 8, sh = bp & 7;
    uint32_t val = (uint32_t)buf[bi];
    if (sh + bits > 8)  val |= (uint32_t)buf[bi + 1] << 8;
    if (sh + bits > 16) val |= (uint32_t)buf[bi + 2] << 16;
    return (int)((val >> sh) & ((1u << bits) - 1u));
}

// Generic flex bit pack (supports 1-10 bits per element)
static __device__ __forceinline__ void tq_flex_pack(uint8_t * buf, int idx, int val, int bits) {
    int bp = idx * bits, bi = bp / 8, sh = bp & 7;
    int v = val & ((1 << bits) - 1);
    buf[bi] |= (uint8_t)(v << sh);
    if (sh + bits > 8)  buf[bi + 1] |= (uint8_t)(v >> (8 - sh));
    if (sh + bits > 16) buf[bi + 2] |= (uint8_t)(v >> (16 - sh));
}


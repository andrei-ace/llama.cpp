        if (tq_flex_hi_bits==5 && tq_flex_lo_bits==3 && tq_flex_hi_res_bits==0 && tq_flex_qjl_hi && !tq_flex_qjl_lo)
            return 60; // GGML_TYPE_TQK_6HI_3LO_HAD (tqk4_sj)

        { int oi = 0, ri = 0;
          for (int j = 0; j < TQK_BLOCK_SIZE_D256; j++) {
              if (tq_is_outlier(mask, j)) hi_raw[oi++] = q[j]; else lo_raw[ri++] = q[j];
          }
        }
        float q_rot_hi[TQK_N_OUTLIER_D256], q_rot_lo[TQK_N_REGULAR_D256];
